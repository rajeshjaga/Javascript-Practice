var bestLang = 'javascript';

//switch statement takes a value/variable to evaluate in case if it matches the condition
switch (bestLang) {
    case 'javascript':
        console.log('YAY! javascript is the best');
        break;
    case 'java':
        console.log('Okay java is best');
        break;
    case 'python':
        console.log('Python might be not be the best');
        break;
    default:
        console.log('There was nothing better');
        break;
}